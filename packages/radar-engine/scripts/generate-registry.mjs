#!/usr/bin/env node
/**
 * Generates packages/radar-engine/src/registry/sources-bulk.ts (~1000 sources).
 * Run: node scripts/generate-registry.mjs
 *
 * Writes a statically-importable TS module (not JSON) so the generated data
 * is traced into serverless bundles by static analysis rather than read from
 * disk at runtime -- see the "registry bulk JSON bundling" incident writeup
 * for why a runtime-read JSON file silently produced an empty array in
 * production.
 */
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, '../src/registry/sources-bulk.ts');

const TIER_KIND = { 0: 'organization-website', 1: 'partner-feed', 2: 'directory', 3: 'feed' };
const TIER_CADENCE = { 0: 168, 1: 72, 2: 48, 3: 24 };

let seq = 0;
function slug(s) {
  return s.toLowerCase().replace(/[^a-z0-9]+/g, '_').replace(/^_|_$/g, '').slice(0, 48);
}
function entry(name, url, verticalId, opts = {}) {
  const tier = opts.tier ?? 0;
  return {
    id: `src_${verticalId}_${slug(name)}_${++seq}`,
    name,
    url,
    kind: opts.kind ?? TIER_KIND[tier],
    verticalId,
    tier,
    opportunityTypes: opts.opportunityTypes ?? ['open-call'],
    disciplines: opts.disciplines,
    geography: opts.geography,
    checkIntervalHours: opts.checkIntervalHours ?? TIER_CADENCE[tier],
    active: true,
    organizationName: tier === 0 ? name : undefined,
    followsOutboundLinks: opts.followsOutboundLinks ?? tier === 2,
    notes: opts.notes,
  };
}
function org(name, url, verticalId, extra = {}) {
  return entry(name, url, verticalId, { tier: 0, ...extra });
}
function platform(name, url, verticalId, extra = {}) {
  return entry(name, url, verticalId, { tier: 1, ...extra });
}
function directory(name, url, verticalId, extra = {}) {
  return entry(name, url, verticalId, { tier: 2, followsOutboundLinks: true, ...extra });
}

